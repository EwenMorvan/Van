// Definition of GPIO attribution for ESP32-S3 

// Digital INPUT
#define BE1 7 // Button BE1 input
#define BE2 15 // Button BE2 input
#define BD1 16 // Button BD1 input
#define BD2 17 // Button BD2 input
#define BH 18 // Button BH input
#define HX_711_DT_A 42 // HX711 DT A input
#define HX_711_DT_B 41 // HX711 DT B input
#define HX_711_DT_C 40 // HX711 DT C input
#define HX_711_DT_D 39 // HX711 DT D input
#define HX_711_DT_E 38 // HX711 DT E input

// Digital OUTPUT
#define I2C_MUX_A0 4 // TCA I2C multiplexer A0
#define I2C_MUX_A1 5 // TCA I2C multiplexer A1
#define I2C_MUX_A2 6 // TCA I2C multiplexer A2
#define REG_MR 3 // Shift register MR
#define REG_DS 14 // Shift register DS
#define REG_STCP 21 // Shift register STCP
#define REG_SHCP 47 // Shift register SHCP
#define REG_OE 48 // Shift register OE
#define HX_711_SCK 1 // HX711 SCK
#define W5500_RST 2 // W5500 RST

// SPI
#define SPI_CS 10 //W5500 CS
#define SPI_MOSI 11 //W5500 MOSI
#define SPI_CLK 12 //W5500 SCK
#define SPI_MISO 13 //W5500 MISO

// I2C
#define I2C_MUX_SDA 8 // TCA I2C multiplexer SDA
#define I2C_MUX_SCL 9 // TCA I2C multiplexer SCL
